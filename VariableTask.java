package Day1;

public class VariableTask {
    public static void main(String[] args) {

        byte by = 10;
        short sh = 1000;
        int in = 200000;
        long lg = 10000000000L;
        float flt = 10.5f;
        double db = 20.123456;
        char ch = 'A';
        boolean boo = true;


        System.out.println("byte value: " + by);
        System.out.println("short value: " + sh);
        System.out.println("int value: " + in);
        System.out.println("long value: " + lg);
        System.out.println("float value: " + flt);
        System.out.println("double value: " + db);
        System.out.println("char value: " + ch);
        System.out.println("boolean value: " + boo);
    }
}
